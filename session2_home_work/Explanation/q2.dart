/*
Describe the difference between
 var, dynamic, and explicitly typed variables in Dart.
*/

// anser
/*
1- var: 
 -It is better to give it a value in the same line.
 -Once a value is assigned during the definition of a variable, 
 its type will be determined based on that first value and cannot be changed.
 -Example: var x = "Hello";  x is String and cannot be changed to another type later.

2- dynamic: 
 - very flexible, you can assign a value of one type and later change it to another type. 

3- explicit type :
 -you declare the variable with a specific type manually
 -This is considered strong typing because the variable is locked to that type
*/